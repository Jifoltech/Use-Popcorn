const average = (arr) =>
  arr.reduce((acc, cur, i, arr) => acc + cur / arr.length, 0);

const KEY = "17bc19e7";

// const tempquery = "mortal-kombat";

export default function App() {
  const [query, setQuery] = useState("");
  const [movies, setMovies] = useState([]);
  const [watched, setWatched] = useState([]);
  const [isLoading, setIsLoading] = useState(false);
  const [error, setError] = useState("");
  const [selectedID, setSelectedID] = useState(null);

  function handleMovieId(id) {
    setSelectedID((selectedID) => (id === selectedID ? null : id));
  }

  function handleMovieClose() {
    setSelectedID(null);
  }

  function handleAddWatched(movie) {
    setWatched((watched) => [...watched, movie]);
  }

  function handleDeleteWached(id) {
    setWatched((watched) => watched.filter((movie) => movie.imdbID !== id));
  }

  useEffect(() => {
    // using Axios to fetch Data

    // const fetchData = async () => {
    //   const res = await axios.get(
    //     `http://www.omdbapi.com/?apikey=${KEY}&s=mortal-kombat`
    //   );
    //   setMovies(res.data.Search);
    // };

    // using Async Function to fetch Data.

    const controller = new AbortController();

    const fetchMovie = async () => {
      try {
        setIsLoading(true);
        setError("");
        const res = await fetch(
          `http://www.omdbapi.com/?apikey=${KEY}&s=${query}`,
          { signal: controller.signal }
        );

        if (!res.ok)
          throw new Error("Something went wrong with fetching Movies");

        const data = await res.json();

        if (data.Response === "False") throw new Error("Movies not found");
        setMovies(data.Search);
        setError("");
        // console.log(data);
      } catch (err) {
        // console.log(err.message);
        if (err.name !== "AbortError") {
          setError(err.message);
        }
      } finally {
        setIsLoading(false);
      }
    };
    if (query.length < 3) {
      setMovies([]);
      setError("");
      return;
    }
    handleMovieClose();
    fetchMovie();
    return () => controller.abort();
  }, [query]);

  return (
    <>
      <Navbar>
        <Logo />
        <SearchInput query={query} setQuery={setQuery} />
        <NumResults movies={movies} />
      </Navbar>
      <Main>
        <Box>
          {isLoading && <Loader />}
          {!isLoading && !error && (
            <MovieList movies={movies} onSelectMovie={handleMovieId} />
          )}
          {error && <ErrorMessage message={error} />}
        </Box>
        <Box>
          {selectedID ? (
            <MovieDetals
              selectedID={selectedID}
              closeMovieDetails={handleMovieClose}
              onAddWatched={handleAddWatched}
              watched={watched}
            />
          ) : (
            <>
              <WatchedSummary watched={watched} />
              <WatchedMovieList
                watched={watched}
                onDeleteWatched={handleDeleteWached}
              />
            </>
          )}
        </Box>
      </Main>
    </>
  );
}

